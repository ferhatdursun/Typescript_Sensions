export interface IStudent {
  isim: string;

  soyad: string;

  sinif: number;

  getStudentInformation(): {//! Burada ki : ile type destegi vermis oluyoruz!
    sen: IStudent["isim"];

    soyad: IStudent["soyad"];

    sinif: IStudent["sinif"];
  };
}

export class Student implements IStudent {
  constructor(
    public isim: string,
    public soyad: string,
    public sinif: number
  ) {}
  getStudentInformation() {
    // return this; oder
    return {
      sen: this.isim,

      soyad: this.soyad,

      sinif: this.sinif,
    };
  }
}

